#adicionando aspas em uma string, abrindo e fechando aspas duplas ou simples
print("Essa e uma 'string' (str).")
print('Essa e uma "string" (str).') #forma convencional
#utilizando caracter de escape
print("Essa e uma \"string\" (str).")
print('Essa e uma \'string\' (str).')
#argumento para nao executar caracteres da string
print(r'Essa e um \n (str).')
#sem o R, o \n executa uma quebra de linha